#define __packed

struct vgroup {
    uint8_t A : 2;
    uint8_t B : 2;
    uint8_t C : 2;
    uint8_t D : 2;
} __packed;

struct lut {
    struct vgroup group[7];
} __packed;

struct group {
    uint8_t phaselength[4];
    uint8_t repeat;
} __packed;

struct waveform {
    struct lut lut[5];
    struct group group[7];
    uint8_t gatelevel;
    uint8_t sourcelevel[3];
    uint8_t dummyline;
    uint8_t gatewidth;
} __packed;

